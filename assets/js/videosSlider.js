let allRecords = document.querySelector('.allRecords')
const loadVideos = (data) =>{
    allRecords.innerHTML=''
    let counter = 0;
    
   for(let i = 0 ; i< Math.ceil(data.length/10) ; i++){
        allRecords.innerHTML+=`
        <div class="swiper-slide recordsContainer"></div>
        `
    let con = document.querySelectorAll('.recordsContainer')
    let re = con[con.length-1]
    if(counter<data.length){
        counter = counter+10
    }
    for( let j = counter-10 ; j<counter ; j++){
        re.innerHTML+=`
        <a class='aRecord' href='../frame3/frame3.html' id=${data[j].id} >
                <div class="record">
                    <div class="img">
                        <img src=${data[j].img} alt="">
                    </div>
                    <p class="description">
                        ${data[j].description}
                    </p>
                </div></a>
        `
    }
    }
    if(data.length<=10){
        document.querySelector('.swiper-pagination').style.display='none'
    }else{
        document.querySelector('.swiper-pagination').style.display='flex'
       }

    //    swiper js
    let mySlider = document.querySelector('.mySlider')
    let swiper_pagination =document.querySelector('.swiper-pagination')
    let swiper_button_next = document.querySelector('.swiper-button-next')
    let swiper_button_prev = document.querySelector('.swiper-button-prev')

    var swiper = new Swiper(mySlider, {
        slidesPerView: 1,
        spaceBetween: 30,
        loop: true,
        pagination: {
         el: swiper_pagination,
         bulletClass: 'swiper-pagination-number',
         bulletActiveClass: 'swiper-pagination-number--active',
         clickable: true,
         renderBullet: function (index, className) {
           return '<h5 class="number-'+ (index + 1) + ' ' + className + '"' + 'index="' + (index + 1) +'">' + (index + 1) + '</h5>';
         },
        },
        navigation: {
          nextEl: swiper_button_next,
          prevEl: swiper_button_prev,
        },
      });
}

